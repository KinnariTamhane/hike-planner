export async function GET(request: NextRequest) {
  try {
    const { searchParams } = new URL(request.url);
    const page = parseInt(searchParams.get("page") || "1");
    const limit = parseInt(searchParams.get("limit") || "10");
    const country = searchParams.get("country");
    const region = searchParams.get("region");
    const search = searchParams.get("search");

    // Try MongoDB first
    try {
      await dbConnect();
      const query: any = {};
      if (country) query.country = country;
      if (region) query.region = region;
      if (search) {
        query.$or = [
          { title: { $regex: search, $options: "i" } },
          { description: { $regex: search, $options: "i" } }
        ];
      }

      const hikes = await Hike.find(query)
        .skip((page - 1) * limit)
        .limit(limit);

      const total = await Hike.countDocuments(query);

      return NextResponse.json(
        {
          hikes,
          total,
          page,
          totalPages: Math.ceil(total / limit),
        },
        { status: 200 }
      );
    } catch (dbError) {
      // Fallback to mock data if MongoDB fails
      console.log("MongoDB connection failed, using mock data");
    }

    // Use mock data as fallback
    let filteredHikes = mockHikes;
    if (country) {
      filteredHikes = filteredHikes.filter((hike) => hike.country === country);
    }
    if (region) {
      filteredHikes = filteredHikes.filter((hike) => hike.region === region);
    }
    if (search) {
      const searchLower = search.toLowerCase();
      filteredHikes = filteredHikes.filter(
        (hike) => 
          hike.title.toLowerCase().includes(searchLower) || 
          hike.description.toLowerCase().includes(searchLower)
      );
    }

    const startIndex = (page - 1) * limit;
    const endIndex = startIndex + limit;
    const paginatedHikes = filteredHikes.slice(startIndex, endIndex);

    return NextResponse.json(
      {
        hikes: paginatedHikes,
        total: filteredHikes.length,
        page,
        totalPages: Math.ceil(filteredHikes.length / limit),
      },
      { status: 200 }
    );
  } catch (error) {
    console.error(error);
    return NextResponse.json(
      { message: "Internal server error" },
      { status: 500 }
    );
  }
}
